package serpent

import (
	"crypto/cipher"
	"strconv"
)

const BlockSize = 16

type KeySizeError int

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/30 17:41:26 ColeCai.                                                                          *
// *********************************************************************************************************************
func (k KeySizeError) Error() string {
	return "serpent: invalid key size " + strconv.Itoa(int(k))
}

type serpent []uint32

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/30 17:41:50 ColeCai.                                                                          *
// *********************************************************************************************************************
func NewCipher(key []byte) (cipher.Block, error) {
	k := len(key)
	switch k {
	case 16, 24, 32:
		break
	default:
		return nil, KeySizeError(k)
	}

	block := make(serpent, 140)
	expandKey(block, key, uint32(k)*8)

	return block, nil
}

