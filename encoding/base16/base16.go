package base16

import (
	"encoding/hex"
	"github.com/caijunjun/gotils/encoding/base"
)

type Base16Encoding struct{}

var StdEncoding = NewBase16Encoding()

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/11/06 22:25:36 ColeCai.                                                                          *
// *********************************************************************************************************************
func NewBase16Encoding() base.IEncoding {
	return &Base16Encoding{}
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/31 00:07:13 ColeCai.                                                                          *
// *********************************************************************************************************************
func (b *Base16Encoding) Encode(src []byte) ([]byte, error) {
	dst := make([]byte, b.EncodedLen(len(src)))
	hex.Encode(dst, src)
	return dst, nil
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/11/06 22:25:57 ColeCai.                                                                          *
// *********************************************************************************************************************
func (b *Base16Encoding) EncodedLen(n int) int {
	return hex.EncodedLen(n)
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/31 00:08:05 ColeCai.                                                                          *
// *********************************************************************************************************************
func (b *Base16Encoding) Decode(src []byte) ([]byte, error) {
	dst := make([]byte, b.DecodedLen(len(src)))
	_, err := hex.Decode(dst, src)
	if err != nil {
		return nil, err
	}
	return dst, nil
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/11/06 22:26:29 ColeCai.                                                                          *
// *********************************************************************************************************************
func (b *Base16Encoding) DecodedLen(n int) int {
	return hex.EncodedLen(n)
}
