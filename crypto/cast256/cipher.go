package cast256

import (
	"crypto/cipher"
	"github.com/caijunjun/gotils/tools"
	"strconv"
)

const BlockSize = 16
const KeySize = 32

type KeySizeError int

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/30 18:20:05 ColeCai.                                                                          *
// *********************************************************************************************************************
func (k KeySizeError) Error() string {
	return "cast256: invalid key size " + strconv.Itoa(int(k))
}

type cast256 struct {
	xk []uint32
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/30 18:20:34 ColeCai.                                                                          *
// *********************************************************************************************************************
func NewCipher(key []byte) (cipher.Block, error) {
	k := len(key)
	if k != KeySize {
		return nil, KeySizeError(k)
	}

	block := &cast256{make([]uint32, 96)}
	expandKey(block.xk, tools.BytesToIntegers[uint32](key), uint32(k))

	return block, nil
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/30 18:21:06 ColeCai.                                                                          *
// *********************************************************************************************************************
func (c *cast256) BlockSize() int {
	return BlockSize
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/30 18:21:18 ColeCai.                                                                          *
// *********************************************************************************************************************
func (c *cast256) Encrypt(dst, src []byte) {
	encrypt(c.xk, tools.BytesToIntegers[uint32](dst), tools.BytesToIntegers[uint32](src))
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/30 18:21:45 ColeCai.                                                                          *
// *********************************************************************************************************************
func (s *cast256) Decrypt(dst, src []byte) {
	decrypt(s.xk, tools.BytesToIntegers[uint32](dst), tools.BytesToIntegers[uint32](src))
}
