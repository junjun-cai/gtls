package mode

import (
	"crypto/cipher"
)

type gcmMode struct {
	nonce   []byte
	gcmAEAD cipher.AEAD
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/27 22:42:11 ColeCai.                                                                          *
// *********************************************************************************************************************
func NewGCMMode(block cipher.Block, iv []byte) (IMode, error) {
	gcm, err := cipher.NewGCMWithNonceSize(block, len(iv))
	if err != nil {
		return nil, err
	}
	return &gcmMode{
		nonce:   iv,
		gcmAEAD: gcm,
	}, nil
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/27 22:46:43 ColeCai.                                                                          *
// *********************************************************************************************************************
func (g *gcmMode) Encode(src []byte) ([]byte, error) {
	return g.gcmAEAD.Seal(nil, g.nonce, src, nil), nil
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/27 22:47:58 ColeCai.                                                                          *
// *********************************************************************************************************************
func (g *gcmMode) Decode(src []byte) ([]byte, error) {
	return g.gcmAEAD.Open(nil, g.nonce, src, nil)
}
