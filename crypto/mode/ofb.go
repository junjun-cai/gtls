package mode

import (
	"crypto/cipher"
	"github.com/pkg/errors"
)

type ofbMode struct {
	stream cipher.Stream
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/27 22:13:00 ColeCai.                                                                          *
// *********************************************************************************************************************
func NewOFBMode(block cipher.Block, iv []byte) (mode IMode, err error) {
	defer func() {
		if r := recover(); r != nil {
			err = errors.Errorf("%+v", r)
			return
		}
		return
	}()
	return &ofbMode{stream: cipher.NewOFB(block, iv)}, nil
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/27 22:13:13 ColeCai.                                                                          *
// *********************************************************************************************************************
func (o *ofbMode) Encode(src []byte) ([]byte, error) {
	encrypted := make([]byte, len(src))
	o.stream.XORKeyStream(encrypted, src)
	return encrypted, nil
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/27 22:13:36 ColeCai.                                                                          *
// *********************************************************************************************************************
func (o *ofbMode) Decode(src []byte) ([]byte, error) {
	decrypted := make([]byte, len(src))
	o.stream.XORKeyStream(decrypted, src)
	return decrypted, nil
}
