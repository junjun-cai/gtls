package hex

import (
	"encoding/hex"
	"github.com/caijunjun/gotils/encoding/base"
)

type HexEncoding struct {
}

var StdEncoding = NewHexEncoding()

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/11/06 22:22:19 ColeCai.                                                                          *
// *********************************************************************************************************************
func NewHexEncoding() base.IEncoding {
	return &HexEncoding{}
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/31 00:03:59 ColeCai.                                                                          *
// *********************************************************************************************************************
func (h *HexEncoding) Encode(src []byte) ([]byte, error) {
	dst := make([]byte, h.EncodedLen(len(src)))
	hex.Encode(dst, src)
	return dst, nil
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/11/06 22:22:48 ColeCai.                                                                          *
// *********************************************************************************************************************
func (h *HexEncoding) EncodedLen(n int) int {
	return hex.EncodedLen(n)
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/31 00:05:52 ColeCai.                                                                          *
// *********************************************************************************************************************
func (h *HexEncoding) Decode(src []byte) ([]byte, error) {
	dst := make([]byte, h.DecodedLen(len(src)))
	_, err := hex.Decode(dst, src)
	if err != nil {
		return nil, err
	}
	return dst, nil
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/11/06 22:23:25 ColeCai.                                                                          *
// *********************************************************************************************************************
func (h *HexEncoding) DecodedLen(n int) int {
	return hex.DecodedLen(n)
}
