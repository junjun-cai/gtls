package ascii85

import (
	"encoding/ascii85"
	"github.com/caijunjun/gotils/encoding/base"
)

type Ascii85 struct{}

var StdEncoding = NewAscii85()

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
