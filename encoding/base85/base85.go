package base85

import (
	"encoding/ascii85"
	"github.com/caijunjun/gotils/encoding/base"
)

type Base85Encoding struct{}

var StdEncoding = NewBase85Encoding()

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/11/06 23:28:13 ColeCai.                                                                          *
// *********************************************************************************************************************
func NewBase85Encoding() base.IEncoding {
	return &Base85Encoding{}
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/31 15:41:47 ColeCai.                                                                          *
// *********************************************************************************************************************
func (b *Base85Encoding) Encode(src []byte) ([]byte, error) {
	buf := make([]byte, b.EncodedLen(len(src)))
	n := ascii85.Encode(buf, src)
	return buf[:n], nil
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/11/06 23:28:46 ColeCai.                                                                          *
// *********************************************************************************************************************
func (b *Base85Encoding) EncodedLen(n int) int {
	return ascii85.MaxEncodedLen(n)
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/31 15:42:52 ColeCai.                                                                          *
// *********************************************************************************************************************
func (b *Base85Encoding) Decode(src []byte) ([]byte, error) {
	buf := make([]byte, b.DecodedLen(len(src)))
	n, _, err := ascii85.Decode(buf, src, true)
	if err != nil {
		return nil, err
	}
	return buf[:n], nil
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/11/06 23:29:17 ColeCai.                                                                          *
// *********************************************************************************************************************
func (b *Base85Encoding) DecodedLen(n int) int {
	return n
}
