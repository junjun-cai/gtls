func NewAscii85() base.IEncoding {
	return &Ascii85{}
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/31 15:45:45 ColeCai.                                                                          *
// *********************************************************************************************************************
func (a *Ascii85) Encode(src []byte) ([]byte, error) {
	buf := make([]byte, a.EncodedLen(len(src)))
	n := ascii85.Encode(buf, src)
	return buf[:n], nil
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/11/06 22:18:18 ColeCai.                                                                          *
// *********************************************************************************************************************
func (a *Ascii85) EncodedLen(n int) int {
	return ascii85.MaxEncodedLen(n)
}

// *********************************************************************************************************************
// * SUMMARY: none.                                                                                                    *
// * WARNING: none.                                                                                                    *
// * HISTORY:                                                                                                          *
// *    -create: 2023/10/31 15:45:58 ColeCai.                                                                          *
// *********************************************************************************************************************
func (a *Ascii85) Decode(src []byte) ([]byte, error) {
	buf := make([]byte, a.DecodedLen(len(src)))
	n, _, err := ascii85.Decode(buf, src, true)
	if err != nil {
		return nil, err
	}
	return buf[:n], nil
}

